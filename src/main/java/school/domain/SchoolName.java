package school.domain;

public class SchoolName {

	private String name;
	private Integer number_of_resource;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getNumber_of_resource() {
		return number_of_resource;
	}
	public void setNumber_of_resource(Integer number_of_resource) {
		this.number_of_resource = number_of_resource;
	}
	
	
}
